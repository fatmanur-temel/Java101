public class Main {

    public static void main(String[] args) {
         //1-10 arasındaki sayıların ekrana yazdırılması
        
        //sayaç tanımlamasını döngünün dışında yapıyoruz
        int i = 1;

        while (i<=10) //döngünün koşulunu belirtiyoruz
        {
            System.out.println(i);
            i++; //sayacın artış miktarını belirtiyoruz
        }
    }
}
